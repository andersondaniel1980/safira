package entity;

public class Usuario {
  private Integer idUsuario;
  private String nome;
  private String email;
  private String senha;
  
  public Usuario() {
  }

public Usuario(Integer idUsuario, String nome, String email, String senha) {
	this.idUsuario = idUsuario;
	this.nome = nome;
	this.email = email;
	this.senha = senha;
}

	
  
} 
